package Logical_Programs;

public class Verify_that_Given_Number_is_Even_or_Odd 
{
	public static void main(String[]args)
	{
		int NUM = 19;
		
		if(NUM%2==0)
		{
			System.out.println(" Given Number "+NUM+" is EVEN");
		}
		else
		{
			System.out.println(" Given Number "+NUM+" is ODD");
		}
	}

}
